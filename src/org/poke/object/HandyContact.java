package org.poke.object;

public class HandyContact {
	
	private String number;
	private String name;
	
	
	public HandyContact(String number, String name){
		
		this.number = number;
		this.name = name;
	}


	public String getNumber() {
		return number;
	}


	public String getName() {
		return name;
	}
	
	
	
}
